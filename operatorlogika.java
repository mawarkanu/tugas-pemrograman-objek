public class operatorlogika {
	public static void main(String[] args) {
		boolean x = true;
		boolean y = false;

		// Operator logika AND (&&)
		boolean result = x && y;  // Hasilnya false
		System.out.println("Hasil x && y = " + result);

		// Operator logika OR (||)
		result = x || y;  // Hasilnya true
		System.out.println("Hasil x || y = " + result);
	}
}